print("........ count from -10 to 10 .... not including 10.........")
for num1 in range(-10, 10):
    print(num1)


print("........ count from -10 to 10...by increment of 2.... not including 10..........")
for num2 in range(-10, 10, 2):
    print(num2)


print("........ count from -10 to -100...by increment of -2.... not including -100..........")
for num3 in range(-10, -100, -2):
    print(num3)
